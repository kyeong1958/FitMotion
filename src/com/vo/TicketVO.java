package com.vo;

public class TicketVO {
	private String  ticket_modified_date    ="";
	private String  ticket_name             ="";
	private String  ticket_num              ="";
	private String  gym_num                 ="";
	private String  ticket_class_time       ="";
	private String  ticket_price            ="";
	
	public String getTicket_modified_date() {
		return ticket_modified_date;
	}
	public void setTicket_modified_date(String ticket_modified_date) {
		this.ticket_modified_date = ticket_modified_date;
	}
	public String getTicket_name() {
		return ticket_name;
	}
	public void setTicket_name(String ticket_name) {
		this.ticket_name = ticket_name;
	}
	public String getTicket_num() {
		return ticket_num;
	}
	public void setTicket_num(String ticket_num) {
		this.ticket_num = ticket_num;
	}
	public String getGym_num() {
		return gym_num;
	}
	public void setGym_num(String gym_num) {
		this.gym_num = gym_num;
	}
	public String getTicket_class_time() {
		return ticket_class_time;
	}
	public void setTicket_class_time(String ticket_class_time) {
		this.ticket_class_time = ticket_class_time;
	}
	public String getTicket_price() {
		return ticket_price;
	}
	public void setTicket_price(String ticket_price) {
		this.ticket_price = ticket_price;
	}
}
